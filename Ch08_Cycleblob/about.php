    <h1>About</h1>
    <p>The concept of cycleblob is based on the lighcycles game from the 1982 movie <a href="http://www.imdb.com/title/tt0084827/">"TRON"</a>.
    In the original movie lightcycles played against each other on a flat grid and could only
    make 90 degree turns while on the grid. Cycleblob takes this idea to the next level and
    makes the grid a three-dimentional object, floating in space.</p>
    <p>Cycleblob is implemented using WebGL and HTML 5 canvas elements and is written completely
    in JavaScript. WebGL is a port of OpenGL ES 2.0 to JavaScript. The latest version of the
    WebGL specification can be found <a href="https://www.khronos.org/registry/webgl/specs/1.0/">here.</a></p>
    <p>Cycleblob was created by <a href="mailto:cycleblob@gmail.com">Shy Shalom</a>, an Israeli developer, as a personal project for learning WebGL and game
    engine design and implementation.<br/>Many thanks to: Terry Hagag, Lior Ashkenazi, Lior Shapira, Noa Simon, Matthew Hagerty,
    Kenneth Russell, Benoit Jacob for various kinds of help, inspiration and support.
    </p>
